  end

  on_linux do
    url "https://github.com/octomil/octomil-python/releases/download/v#{version}/octomil-linux-amd64.tar.gz"
    sha256 "2fdc81324f2880407ed1feb0a5fb4dcebc9e38aed90f5ccde3d0c79eecfcfd5a"
  end

  def install
